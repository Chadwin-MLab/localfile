# A simple for loop. Because we printing the sequence of the range, it should print the range starting from 0 for 4 digits

for i in range(4):
    print(i)
    
# Now lets print my name 5 times

for i in range(5):
    print("Chadwin")
    
# Using a for loop to print an iteration of letters, will spell the letters out

for i in "Chadwin":
    print(i)