#Importing the math module and making use of the various functions

import math

#Sqrt-Printing the square root of 4

print(math.sqrt(4))

#Fabs-Printing the absolute value of (-5)

print(math.fabs(-5))

#floor- Printing the largest integer less than or equal to (4.6)

print(math.floor(4.6))

#Pow-print the power of (2,2)

print(math.pow(2,2))

#trunc-Print the truncated value of (7.9999)

print(math.trunc(7.999))